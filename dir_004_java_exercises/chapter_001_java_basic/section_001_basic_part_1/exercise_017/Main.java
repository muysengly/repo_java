public class Main {
    // ប្រកាសថ្នាក់សាធារណៈមានឈ្មោះ Main
    // Declares a public class named Main

    public static void main(String[] args) {
        // ប្រកាស method main ដែលជាចំណុចចាប់ផ្តើមនៃកម្មវិធី
        // Declares the main method, the entry point of the program

        double x = 10.01;
        // ប្រកាសអថេរទាំងស្រុង x និងផ្ដល់តម្លៃដំបូង 10.01
        // Declare a double variable x and initialize it to 10.01

        System.out.println(x);
        // បង្ហាញតម្លៃនៃ x ទៅកាន់ console
        // Print the value of x to the console

        System.out.println((int) x);
        // បង្ហាញតម្លៃនៃ x បន្ទាប់ពីបម្លែងទៅ int ទៅកាន់ console
        // Print the value of x after casting it to an int to the console
    }
    // បិទ method main
    // Closes the main method
}
// បិទថ្នាក់ Main
// Closes the Main class